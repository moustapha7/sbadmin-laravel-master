     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        if($request->id){
            $team = Team::find($request->id);
            $team->name = $request->name;
            $team->description = $request->description;
            if($team->save()){
                Session::flash('success','Team update successful');
            }else{
                Session::flash('error','Team update error');
            }

        }else{
            if(Team::create($request->all())){
                Session::flash('success','Team create successful');
            }else{
                Session::flash('error','Team create error');
            }
        }
        return view('admin.newTeam');
    }

